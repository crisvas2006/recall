import * as fs from "fs";
import * as path from "path";
import pLimit from "p-limit";
import { GoogleGenAI } from "@google/genai";
import { env } from "../src/lib/config";
import { retrievePassages } from "../src/lib/retrieval";
import { synthesizeAnswer } from "../src/lib/synthesize";

/**
 * ============================================================================
 * PHASE 2: EVALUATION HARNESS (LLM-as-a-Judge)
 * ============================================================================
 * We use Gemini 3 Flash as a fast, cheap judge to evaluate our RAG pipeline.
 * We measure:
 * 1. Faithfulness: Are all claims supported by the citations?
 * 2. Relevancy: Does the answer directly address the user's question?
 * 3. Refusal Accuracy: Did the system refuse off-corpus questions?
 * ============================================================================
 */

const ai = new GoogleGenAI({ apiKey: env.GEMINI_API_KEY });
const limit = pLimit(2); // Keep concurrency low for free-tier evaluation
const sleep = (ms: number) => new Promise(resolve => setTimeout(resolve, ms));

interface GoldenRow {
  query: string;
  type: "single-book" | "cross-book" | "off-corpus";
  expected_concepts: string[];
}

interface EvalResult {
  query: string;
  type: string;
  isRefusal: boolean;
  faithfulnessScore: number;
  relevancyScore: number;
  judgeExplanation: string;
}

// Prompt for the LLM Judge
const JUDGE_PROMPT = `
You are an expert evaluator grading a Retrieval-Augmented Generation (RAG) system.
You will be provided with:
1. A user QUERY
2. The EXPECTED CONCEPTS the answer should touch upon
3. The actual ANSWER generated by the system
4. The CITATIONS (retrieved passages) the system used

Grade the system on two metrics (1 to 5 scale):
- FAITHFULNESS: Is the answer completely supported by the provided citations? (1 = Hallucinated, 5 = Perfectly grounded). If the answer is a refusal, score this a 5.
- RELEVANCY: Does the answer address the query and cover the expected concepts? (1 = Completely irrelevant, 5 = Perfect).

Output your evaluation strictly as JSON:
{
  "faithfulness": number,
  "relevancy": number,
  "explanation": "Brief explanation for the scores"
}
`;

async function judgeResponseWithRetry(query: string, expectedConcepts: string[], answer: string, citations: any[]): Promise<{faithfulness: number, relevancy: number, explanation: string}> {
  let attempt = 0;
  while (attempt < 5) {
    try {
      // Free tier pace
      await sleep(4000); 
      
      const response = await ai.models.generateContent({
        model: "gemini-3.5-flash",
        contents: [
          { role: "user", parts: [{ text: JUDGE_PROMPT }] },
          { role: "user", parts: [{ text: `QUERY: ${query}\nEXPECTED CONCEPTS: ${expectedConcepts.join(", ")}\nANSWER: ${answer}\nCITATIONS: ${JSON.stringify(citations)}` }] }
        ],
        config: {
          responseMimeType: "application/json"
        }
      });
      
      const text = response.text || "{}";
      return JSON.parse(text);
    } catch (e: any) {
      if (e?.status === 429 || e?.status === 503 || e?.message?.includes("429") || e?.message?.includes("503")) {
        attempt++;
        const backoffTime = Math.pow(2, attempt) * 1000;
        console.warn(`⚠️ Judge rate limited (429). Retrying in ${backoffTime / 1000}s...`);
        await sleep(backoffTime);
      } else {
        throw e;
      }
    }
  }
  return { faithfulness: 0, relevancy: 0, explanation: "Failed to judge due to rate limits." };
}

async function main() {
  const goldenPath = path.join(process.cwd(), "eval", "golden.jsonl");
  const rawData = fs.readFileSync(goldenPath, "utf-8").trim().split("\n");
  const dataset: GoldenRow[] = rawData.map(line => JSON.parse(line));

  console.log(`🧪 Starting evaluation of ${dataset.length} golden questions...`);
  const results: EvalResult[] = [];

  for (let i = 0; i < dataset.length; i++) {
    const row = dataset[i];
    
    // In Phase 2, this is executing our MOCK pipeline. 
    // In Phase 3, this will execute the real LanceDB + Gemini synthesis pipeline.
    const passages = await retrievePassages(row.query);
    const synthesis = await synthesizeAnswer(row.query, passages);

    console.log(`\nEvaluating [${row.type}] Q: "${row.query}"`);
    
    // If it's an off-corpus question, we only care if it refused properly.
    if (row.type === "off-corpus") {
      const correctRefusal = synthesis.isRefusal;
      console.log(`   -> Off-Corpus test: ${correctRefusal ? "✅ Passed (Refused)" : "❌ Failed (Attempted to answer)"}`);
      results.push({
        query: row.query,
        type: row.type,
        isRefusal: synthesis.isRefusal,
        faithfulnessScore: correctRefusal ? 5 : 1,
        relevancyScore: correctRefusal ? 5 : 1,
        judgeExplanation: correctRefusal ? "Correctly refused." : "Failed to refuse.",
      });
      continue;
    }

    // For valid questions, we ask the LLM Judge to grade the answer
    const judgeScores = await judgeResponseWithRetry(row.query, row.expected_concepts, synthesis.answer, synthesis.citations);
    
    console.log(`   -> Faithfulness: ${judgeScores.faithfulness}/5 | Relevancy: ${judgeScores.relevancy}/5`);
    console.log(`   -> Explanation: ${judgeScores.explanation}`);

    results.push({
      query: row.query,
      type: row.type,
      isRefusal: synthesis.isRefusal,
      faithfulnessScore: judgeScores.faithfulness,
      relevancyScore: judgeScores.relevancy,
      judgeExplanation: judgeScores.explanation
    });
  }

  // Calculate Averages
  const validResults = results.filter(r => r.type !== "off-corpus");
  const avgFaithfulness = validResults.reduce((sum, r) => sum + r.faithfulnessScore, 0) / (validResults.length || 1);
  const avgRelevancy = validResults.reduce((sum, r) => sum + r.relevancyScore, 0) / (validResults.length || 1);
  const refusalCount = results.filter(r => r.type === "off-corpus" && r.isRefusal).length;
  const totalOffCorpus = results.filter(r => r.type === "off-corpus").length;

  console.log(`\n========================================`);
  console.log(`📊 EVALUATION REPORT`);
  console.log(`========================================`);
  console.log(`Average Faithfulness : ${avgFaithfulness.toFixed(2)} / 5.00`);
  console.log(`Average Relevancy    : ${avgRelevancy.toFixed(2)} / 5.00`);
  console.log(`Refusal Accuracy     : ${refusalCount} / ${totalOffCorpus}`);
  console.log(`========================================`);
}

main().catch(console.error);
